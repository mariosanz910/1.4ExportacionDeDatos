public class Estudiante {
    int id;
    String nombre;
    String apellidos;
    int edad;
    double nota;

    public Estudiante(int id, String nombre, String apellidos, int edad, double nota) {
        this.id = id;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
        this.nota = nota;
    }
}
